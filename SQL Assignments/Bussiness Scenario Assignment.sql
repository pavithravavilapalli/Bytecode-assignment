-- 21. Find customers whose purchases dropped continuously 3 times
SELECT *
FROM
(
    SELECT CUSTOMER_NAME,ORDER_DATE,SALES_AMOUNT,
           LAG(SALES_AMOUNT,1)OVER(PARTITION BY CUSTOMER_NAME ORDER BY ORDER_DATE) AS PREV1,
