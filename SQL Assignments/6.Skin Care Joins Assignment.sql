/*61. Create customer segmentation:
Premium
Regular
Low Value
Based on total spending.
*/
SELECT 
      C.CUSTOMER_NAME,
      SUM(O.QUANTITY * P.PRICE) AS TOTAL_SPENDING,
      CASE
      WHEN SUM(O.QUANTITY * P.PRICE) >= 2000 THEN 'Premium'
      WHEN SUM(O.QUANTITY * P.PRICE) >= 1000 THEN 'Regular'
      ELSE 'Low Value'
      END AS SEGMENT
      FROM CUSTOMERS C 
      INNER JOIN ORDERS O ON C.CUSTOMER_ID = O.CUSTOMER_ID
      INNER JOIN PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
      GROUP BY C.CUSTOMER_NAME;

